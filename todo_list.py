# ================================================
# Название: Список дел (To-Do List)
# Описание: Программа запрашивает количество дел,
#           затем сами дела и выводит их нумерованным списком.
# Технологии: Python, списки, циклы, обработка ошибок
# ================================================

def main():
    print("Добро пожаловать в менеджер задач!")
    
    while True:
        try:
            n = int(input("Сколько дел у тебя сегодня? "))
            if n <= 0:
                print("Введи число больше 0!")
                continue
            break
        except ValueError:
            print("Ошибка! Введи целое число.")

    tasks = []
    for i in range(n):
        task = input(f"Дело {i+1}: ")
        tasks.append(task)

    print("\nТвои дела на сегодня:")
    for i, task in enumerate(tasks, 1):
        print(f"{i}. {task}")

if __name__ == "__main__":
    main()